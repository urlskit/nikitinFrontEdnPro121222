const number1 = parseInt (prompt('Введите первое число'));

const number2 = parseInt (prompt('Введите второе число'));

const sum_p = number1 + number2;
const sum_m = number1 - number2;
const sum_u = number1 * number2;
const sum_r = number1 / number2;

alert(`${number1} + ${number2} = ${sum_p} \n ${number1} - ${number2} = ${sum_m} \n ${number1} * ${number2} = ${sum_u} \n ${number1} / ${number2} = ${sum_r}`);